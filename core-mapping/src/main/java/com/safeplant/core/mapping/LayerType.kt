package com.safeplant.core.mapping

/**
 * Типы слоев карты
 * Определяет доступные типы слоев для отображения на карте
 */
enum class LayerType {
    /**
     * Слой опасных зон (красные полигоны)
     */
    HAZARDS,
    
    /**
     * Слой предустановленных маршрутов (синие линии)
     */
    ROUTES,
    
    /**
     * Слой эвакуации (зеленые линии)
     */
    EVACUATION,
    
    /**
     * Слой точек СИЗ (желтые иконки)
     */
    PPE_POINTS
}